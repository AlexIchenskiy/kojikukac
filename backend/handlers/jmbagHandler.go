package handlers

import (
	"Projekt/config"
	"net/http"
)

func JmbagHandler(w http.ResponseWriter, r *http.Request) {
	conf, _ := config.GetConfigFromYaml()

	_, err := w.Write([]byte(conf.JmbagConf))
	if err != nil {
		http.Error(w, "Failed to write response", http.StatusInternalServerError)
		return
	}
}
